public class StringExercise {
    public static void main(String[] args) {
        String TheWall = "We don't need no education %nWe don't need no thought control";
        System.out.printf(TheWall);

        String quotes = "Check \"this\" out!, \"s inside of \"s!";
        System.out.printf("%n" + quotes);

        String windows = "In windows,the main drive is usually C:\\";
        System.out.printf("%n" + windows);

        String backslashes = "I can do backslashes \\, double backslashes \\\\, %nand the amazing triple backslash \\\\\\!";
        System.out.printf("%n" + backslashes);
    }
}
